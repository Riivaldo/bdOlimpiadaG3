-- TABLA PERSONA
CREATE TABLE Persona (
    idPersona    INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    ci           VARCHAR(20)  NOT NULL,
    nombre       VARCHAR(100) NOT NULL,
    apePaterno   VARCHAR(100) NOT NULL,
    apeMaterno   VARCHAR(100),
    fechaNac     DATE,
    telefono     VARCHAR(20),
    CONSTRAINT pk_persona PRIMARY KEY (idPersona)
);

-- TABLA TUTOR
CREATE TABLE Tutor (
    idPersona    INTEGER NOT NULL, -- No lleva identity porque depende de Persona
    especialidad VARCHAR(100),
    CONSTRAINT pk_tutor PRIMARY KEY (idPersona),
    CONSTRAINT fk_tutor_persona FOREIGN KEY (idPersona) REFERENCES Persona(idPersona)
);

-- TABLA COLEGIO
CREATE TABLE Colegio (
    idColegio     INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    nombre        VARCHAR(100) NOT NULL,
    direccion     VARCHAR(200),
    municipio     VARCHAR(100),
    nroAprobados  INTEGER,
    nroReprobados INTEGER,
    tipo          VARCHAR(10)  NOT NULL,
    idTutor       INTEGER    NOT NULL,
    CONSTRAINT pk_colegio PRIMARY KEY (idColegio),
    CONSTRAINT chk_colegio_tipo CHECK (tipo IN ('fiscal','particular')),
    CONSTRAINT fk_colegio_tutor FOREIGN KEY (idTutor) REFERENCES Tutor(idPersona)
);

-- TABLA PARTICIPANTE
CREATE TABLE Participante (
    idPersona         INTEGER NOT NULL, -- Depende de Persona
    grado_Escolar     VARCHAR(50),
    idColegio         INTEGER NOT NULL,
    idTutor_Encargado INTEGER NOT NULL,
    CONSTRAINT pk_participante PRIMARY KEY (idPersona),
    CONSTRAINT fk_part_persona FOREIGN KEY (idPersona) REFERENCES Persona(idPersona),
    CONSTRAINT fk_part_colegio FOREIGN KEY (idColegio) REFERENCES Colegio(idColegio),
    CONSTRAINT fk_part_tutor FOREIGN KEY (idTutor_Encargado) REFERENCES Tutor(idPersona)
);

-- TABLA OLIMPIADA
CREATE TABLE Olimpiada (
    idOlimpiada INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    nombre      VARCHAR(200) NOT NULL,
    gestion     INTEGER     NOT NULL,
    version     VARCHAR(50),
    fecha_Ini   DATE          NOT NULL,
    fecha_Fin   DATE          NOT NULL,
    CONSTRAINT pk_olimpiada PRIMARY KEY (idOlimpiada)
);

-- TABLA AREA
CREATE TABLE Area (
    idArea INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    nombre VARCHAR(100) NOT NULL,
    CONSTRAINT pk_area PRIMARY KEY (idArea)
);

-- TABLA DISPONE
CREATE TABLE Dispone (
    idOlimpiada INTEGER NOT NULL,
    idArea      INTEGER NOT NULL,
    CONSTRAINT pk_dispone PRIMARY KEY (idOlimpiada, idArea),
    CONSTRAINT fk_disp_olimpiada FOREIGN KEY (idOlimpiada) REFERENCES Olimpiada(idOlimpiada),
    CONSTRAINT fk_disp_area FOREIGN KEY (idArea) REFERENCES Area(idArea)
);

-- TABLA INSCRIPCION
CREATE TABLE Inscripcion (
    idInscripcion  INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    costo          NUMERIC NOT NULL, -- Cambiado a NUMERIC para dinero
    estado         VARCHAR(20) NOT NULL,
    idParticipante INTEGER NOT NULL,
    CONSTRAINT pk_inscripcion PRIMARY KEY (idInscripcion),
    CONSTRAINT chk_inscripcion_estado CHECK (estado IN ('pendiente','confirmada','anulada')),
    CONSTRAINT fk_insc_participante FOREIGN KEY (idParticipante) REFERENCES Participante(idPersona)
);

-- TABLA CORRESPONDE
CREATE TABLE Corresponde (
    idOlimpiada   INTEGER NOT NULL,
    idArea        INTEGER NOT NULL,
    idInscripcion INTEGER NOT NULL,
    CONSTRAINT pk_corresponde PRIMARY KEY (idOlimpiada, idArea, idInscripcion),
    CONSTRAINT fk_corr_olimpiada FOREIGN KEY (idOlimpiada) REFERENCES Olimpiada(idOlimpiada),
    CONSTRAINT fk_corr_area FOREIGN KEY (idArea) REFERENCES Area(idArea),
    CONSTRAINT fk_corr_inscripcion FOREIGN KEY (idInscripcion) REFERENCES Inscripcion(idInscripcion)
);

-- TABLA PAGO
CREATE TABLE Pago (
    idPago        INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    monto         NUMERIC NOT NULL,
    fecha_pago    DATE NOT NULL,
    metodo_pago   VARCHAR(50) NOT NULL,
    idInscripcion INTEGER NOT NULL,
    CONSTRAINT pk_pago PRIMARY KEY (idPago),
    CONSTRAINT fk_pago_inscripcion FOREIGN KEY (idInscripcion) REFERENCES Inscripcion(idInscripcion)
);

-- TABLA RESULTADO
CREATE TABLE Resultado (
    idResultado      INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    puntaje_Obtenido INTEGER NOT NULL,
    idInscripcion    INTEGER NOT NULL,
    CONSTRAINT pk_resultado PRIMARY KEY (idResultado),
    CONSTRAINT fk_result_inscripcion FOREIGN KEY (idInscripcion) REFERENCES Inscripcion(idInscripcion)
);

